/**
 * 接口：Logo 组件 Props
 */
export interface ILogoProps {
  /**
   * 属性：图标 SVG 的 class
   * 描述：用于给第一个（圆形图标）svg 设置 class
   */
  classIconArea?: TVueClass;

  /**
   * 属性：所有文字的 class
   * 描述：用于给所有文字统一设置 class
   */
  classTextArea?: TVueClass;

  /**
   * 属性：所有文字的 class
   * 描述：用于给所有文字统一设置 class
   */
  classTexts?: TVueClass;

  /**
   * 属性：第一个文字的 class
   * 描述：用于给第一个文字单独设置 class
   */
  classText1?: TVueClass;

  /**
   * 属性：第二个文字的 class
   * 描述：用于给第二个文字单独设置 class
   */
  classText2?: TVueClass;

  /**
   * 属性：第三个文字的 class
   * 描述：用于给第三个文字单独设置 class
   */
  classText3?: TVueClass;

  /**
   * 属性：第四个文字的 class
   * 描述：用于给第四个文字单独设置 class
   */
  classText4?: TVueClass;
}
